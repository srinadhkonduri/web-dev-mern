// comparison operators
let value1 = 12;
let value2 = 13;

let count1 = "12";
let count2 = "13";


let isEqualValue = value1 == value2;
console.log(isEqualValue);


let isStrictlyEqual = value1 === value2;
console.log(isStrictlyEqual);


let isNotEqual = count1 != count2;
console.log(isNotEqual);

let isStrictlyNotEqual = count1 !== count2;
console.log(isStrictlyNotEqual);

let isGreaterThan = value1 > value2;
console.log(isGreaterThan);

let isGreaterThanOrEqual = count1 >= count2;
console.log(isGreaterThanOrEqual);


let lessThen = value1 < value2;
console.log(lessThen);

let lessThenOrEqual = value1 <= value2;
console.log(lessThenOrEqual);










